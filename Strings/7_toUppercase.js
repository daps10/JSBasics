// Javascript String toUpperCase()

// The toUpperCase() method returns the string converted to uppercase.

// Example
const message = "javascript is fun";

// convert message to uppercase
const upperMessage = message.toUpperCase();
console.log(upperMessage); // Output: JAVASCRIPT IS FUN

/*
==== toUpperCase() Parameters
- The toUpperCase() method does not take in any parameters.

==== toUpperCase() Return Value
- Returns a new string representing the calling string 
converted to uppercase.

- The toUpperCase() method raises TypeError when called 
on null or undefined.

*/